
// Java program to find largest among two
// numbers using ternary operator

public class java49 {
    public static void main(String[] args) {
        int a=7, b=78, max;
        System.out.println("first a:"+a);
         System.out.println("second b :"+ b);
         max=(a>b)?a:b;
         System.out.println("maximum :" + max);
        
    }
    
}
